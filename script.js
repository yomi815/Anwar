function initFeedbackWidget(){
  const noBtn = document.getElementById("feedbackNo");
  const yesBtn = document.getElementById("feedbackYes");
  const box = document.getElementById("feedbackBox");
  const msg = document.getElementById("feedbackMsg");
  if(!noBtn || !yesBtn || !box) return;

  function dodge(){
    const boxRect = box.getBoundingClientRect();
    const btnRect = noBtn.getBoundingClientRect();
    const maxX = Math.max(0, boxRect.width - btnRect.width);
    const maxY = Math.max(0, boxRect.height - btnRect.height);
    const newX = Math.random() * maxX;
    const newY = Math.random() * maxY;
    noBtn.style.position = "absolute";
    noBtn.style.left = newX + "px";
    noBtn.style.top = newY + "px";
  }

  box.style.position = "relative";
  noBtn.addEventListener("mouseenter", dodge);
  noBtn.addEventListener("touchstart", (e) => { e.preventDefault(); dodge(); }, { passive:false });
  noBtn.addEventListener("focus", dodge);
  // منع أي محاولة نقر فعلية بالكيبورد أيضًا
  noBtn.addEventListener("click", (e) => { e.preventDefault(); dodge(); });

  yesBtn.addEventListener("click", () => {
    msg.textContent = "🎉 يسعدنا جدًا أنك أعجبت بالموقع! شكرًا لزيارتك.";
    noBtn.style.display = "none";
    yesBtn.disabled = true;
  });
}

