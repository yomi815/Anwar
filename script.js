const quizState = { index:0, score:0, order: shuffle(QUIZ_GAME.map((_,i)=>i)) };

function initQuizGame(){
  if(!document.getElementById("quizQuestion")) return;
  renderQuizGame();
  document.getElementById("quizRestart").addEventListener("click", () => {
    quizState.index = 0;
    quizState.score = 0;
    quizState.order = shuffle(QUIZ_GAME.map((_,i)=>i));
    renderQuizGame();
  });
}

function renderQuizGame(){
  const total = QUIZ_GAME.length;
  if(quizState.index >= total){
    document.getElementById("quizBody").classList.add("hidden");
    const endEl = document.getElementById("quizEnd");
    endEl.classList.remove("hidden");
    document.getElementById("quizEndMsg").textContent =
      `أجبت بشكل صحيح على ${quizState.score} من ${total} أسئلة.`;
    document.getElementById("quizProgressBar").style.width = "100%";
    return;
  }
  document.getElementById("quizBody").classList.remove("hidden");
  document.getElementById("quizEnd").classList.add("hidden");

  const q = QUIZ_GAME[quizState.order[quizState.index]];
  document.getElementById("quizProgressBar").style.width = Math.round((quizState.index/total)*100) + "%";
  document.getElementById("quizQCount").textContent = `السؤال ${quizState.index+1} من ${total}`;
  document.getElementById("quizQuestion").textContent = q.q;
  document.getElementById("quizScore").textContent = quizState.score;

  const optsEl = document.getElementById("quizOptions");
  optsEl.innerHTML = "";
  shuffle(q.options.map((_,i)=>i)).forEach(optIdx => {
    const btn = document.createElement("button");
    btn.className = "quiz-opt";
    btn.textContent = q.options[optIdx];
    btn.addEventListener("click", () => {
      const buttons = [...optsEl.children];
      buttons.forEach(b => b.disabled = true);
      if(optIdx === q.correct){
        btn.classList.add("correct");
        quizState.score++;
      } else {
        btn.classList.add("wrong");
        buttons.find(b => b.textContent === q.options[q.correct])?.classList.add("correct");
      }
      document.getElementById("quizScore").textContent = quizState.score;
      setTimeout(() => { quizState.index++; renderQuizGame(); }, 1100);
    });
    optsEl.appendChild(btn);
  });
}

