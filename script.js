const memoryState = { first:null, second:null, lock:false, matched:0 };

function initMemoryGame(){
  const board = document.getElementById("memoryBoard");
  if(!board) return;
  buildMemoryBoard();
  document.getElementById("memoryReset").addEventListener("click", buildMemoryBoard);
}

function buildMemoryBoard(){
  const board = document.getElementById("memoryBoard");
  board.innerHTML = "";
  memoryState.first = null;
  memoryState.second = null;
  memoryState.lock = false;
  memoryState.matched = 0;
  document.getElementById("memoryWin").classList.add("hidden");

  const deck = shuffle([...MEMORY_ITEMS, ...MEMORY_ITEMS]);
  deck.forEach((item, i) => {
    const cell = document.createElement("button");
    cell.className = "memory-cell";
    cell.dataset.key = item.key;
    cell.dataset.icon = item.icon;
    cell.innerHTML = `<span class="memory-back">🌟</span><span class="memory-front hidden">${item.icon}</span>`;
    cell.addEventListener("click", () => flipMemoryCell(cell));
    board.appendChild(cell);
  });
}

function flipMemoryCell(cell){
  if(memoryState.lock || cell.classList.contains("flipped") || cell.classList.contains("matched")) return;

  cell.classList.add("flipped");
  cell.querySelector(".memory-back").classList.add("hidden");
  cell.querySelector(".memory-front").classList.remove("hidden");

  if(!memoryState.first){
    memoryState.first = cell;
    return;
  }
  memoryState.second = cell;
  memoryState.lock = true;

  if(memoryState.first.dataset.key === memoryState.second.dataset.key){
    memoryState.first.classList.add("matched");
    memoryState.second.classList.add("matched");
    memoryState.matched++;
    resetMemorySelection();
    if(memoryState.matched === MEMORY_ITEMS.length){
      document.getElementById("memoryWin").classList.remove("hidden");
    }
  } else {
    setTimeout(() => {
      [memoryState.first, memoryState.second].forEach(c => {
        c.classList.remove("flipped");
        c.querySelector(".memory-back").classList.remove("hidden");
        c.querySelector(".memory-front").classList.add("hidden");
      });
      resetMemorySelection();
    }, 900);
  }
}
function resetMemorySelection(){
  memoryState.first = null;
  memoryState.second = null;
  memoryState.lock = false;
}

